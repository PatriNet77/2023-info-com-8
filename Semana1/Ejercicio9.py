print("Haremos operaciones básicas con 2 números")
num_1 = float(input("Ingrese un número: "))
num_2 = float(input("Ingrese otro número: "))
suma = num_1 + num_2
resta = num_1 - num_2
multi = num_1 * num_2
divi = num_1 / num_2
print("El resultado de la suma es: {}" .format(suma),"\nEl resultado de la resta es: {}" .format(resta),"\nEl resultado de la multiplicación es: {}" .format(multi),"\nEl resultado de la división es : {}" .format(divi))
